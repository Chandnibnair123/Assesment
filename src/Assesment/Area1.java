package Assesment;

class Area
{
	public void ar(int a)
	{
		System.out.println("Area of square="+(a*a));
	}
	public void ar(int a ,int b)
	{
		System.out.println("Area of rectangle="+(a*b));
	}
}

public class Area1
{
	public static void main(String[] args)
	{
		Area a = new Area();
		a.ar(2,3);
		a.ar(2);
	}
}






				
		
				
	
	
	


	
		
		
	
	


	